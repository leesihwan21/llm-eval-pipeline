def collect_data():
    print(f"총 {len(prompts)}개 프롬프트로 데이터 수집 시작...")
    
    for i, prompt in enumerate(prompts):
        try:
            response = requests.post(f"{API_URL}/api/eval/run", json={
                "prompt": prompt,
                "models": ["claude-sonnet-4-6", "gpt-4o-mini"],
                "task_type": "general"
            })
            print(f"[{i+1}/{len(prompts)}] 완료: {prompt[:20]}...")
            time.sleep(2)  # API 과부하 방지
        except Exception as e:
            print(f"에러: {e}")

